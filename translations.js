let currentLang = 'en';

/**
 * Get translated product description with fallback to English
 * @param {string} descriptionKey - The description key (e.g., 'desc_classic_design')
 * @param {string} lang - The language code (optional, uses currentLang if not provided)
 * @returns {string} The translated description or the key if not found
 */
function getProductDescription(descriptionKey, lang = null) {
    const language = lang || currentLang;

    // Try to get translation in current language
    if (translations[language] && translations[language][descriptionKey]) {
        return translations[language][descriptionKey];
    }

    // Fallback to English
    if (translations.en && translations.en[descriptionKey]) {
        return translations.en[descriptionKey];
    }

    // If still not found, return the original description text (for backwards compatibility)
    return descriptionKey.replace('desc_', '').replace(/_/g, ' ');
}

function translatePage(lang) {
    console.log('🌍 Translating page to:', lang);

    // Validate language exists
    if (!translations[lang]) {
        console.error('❌ Language not found:', lang, '- Falling back to English');
        lang = 'en';
    }

    currentLang = lang;

    // Translate text content elements
    const elements = document.querySelectorAll('[data-i18n]');
    console.log('📝 Translating', elements.length, 'text elements');

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Translate placeholder attributes for form inputs
    const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
    console.log('📝 Translating', placeholderElements.length, 'placeholder elements');

    placeholderElements.forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });

    // Update both language dropdowns to keep them in sync
    const langDropdown = document.getElementById('languageSelect');
    const langDropdownTop = document.getElementById('languageSelectTop');

    if (langDropdown) {
        langDropdown.value = lang;
        console.log('✅ Updated languageSelect dropdown to:', lang);
    }

    if (langDropdownTop) {
        langDropdownTop.value = lang;
        console.log('✅ Updated languageSelectTop dropdown to:', lang);
    }

    // Update html lang attribute for proper font rendering (especially for Chinese, Hindi, Arabic)
    document.documentElement.setAttribute('lang', lang);

    // Save preference
    localStorage.setItem('fordipsTechLang', lang);
    console.log('💾 Saved language preference:', lang);

    console.log('✅ Translation complete!');
}

