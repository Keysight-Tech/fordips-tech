let currentLang = 'en';

function translatePage(lang) {
    console.log('üåç Translating page to:', lang);

    // Validate language exists
    if (!translations[lang]) {
        console.error('‚ùå Language not found:', lang, '- Falling back to English');
        lang = 'en';
    }

    currentLang = lang;

    // Translate text content elements
    const elements = document.querySelectorAll('[data-i18n]');
    console.log('üìù Translating', elements.length, 'text elements');

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Translate placeholder attributes for form inputs
    const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
    console.log('üìù Translating', placeholderElements.length, 'placeholder elements');

    placeholderElements.forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });

    // Update both language dropdowns to keep them in sync
    const langDropdown = document.getElementById('languageSelect');
    const langDropdownTop = document.getElementById('languageSelectTop');

    if (langDropdown) {
        langDropdown.value = lang;
        console.log('‚úÖ Updated languageSelect dropdown to:', lang);
    }

    if (langDropdownTop) {
        langDropdownTop.value = lang;
        console.log('‚úÖ Updated languageSelectTop dropdown to:', lang);
    }

    // Update html lang attribute for proper font rendering (especially for Chinese, Hindi, Arabic)
    document.documentElement.setAttribute('lang', lang);

    // Save preference
    localStorage.setItem('fordipsTechLang', lang);
    console.log('üíæ Saved language preference:', lang);

    console.log('‚úÖ Translation complete!');
}

// Get current translation
