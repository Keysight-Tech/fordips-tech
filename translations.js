let currentLang = 'en';

function translatePage(lang) {
    currentLang = lang;
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Update language dropdown
    const langDropdown = document.getElementById('languageSelect');
    if (langDropdown) {
        langDropdown.value = lang;
    }

    // Save preference
    localStorage.setItem('fordipsTechLang', lang);
}

// Get current translation
function t(key) {
    return translations[currentLang]?.[key] || translations['en'][key] || key;
}

// Initialize language
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('fordipsTechLang') || 'en';
    translatePage(savedLang);

    // Language dropdown
    const langDropdown = document.getElementById('languageSelect');
    if (langDropdown) {
        langDropdown.addEventListener('change', (e) => {
            const lang = e.target.value;
            translatePage(lang);

            // Show notification
            const messages = {
                en: 'Language changed to English',
                fr: 'Langue changée en français',
                pcm: 'We don change to Pidgin'
            };

            if (typeof showNotification === 'function') {
                showNotification(messages[lang] || messages.en, 'success');
            }
        });
    }
});

// Make t() function globally available
window.t = t;
window.translatePage = translatePage;
